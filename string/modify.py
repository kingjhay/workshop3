string = "Hello, World!"

print(string.upper())  # output : "HELLO, WORLD!"
print(string.lower())  # output : "hello, world!"
print(string.strip())  # output : "Hello, World!"
print(string.replaced("H", "J"))  # output : "Jello, World!"
print(string.split(","))  # output : ['Hello','Wrold!']
print(len(string))  # 15